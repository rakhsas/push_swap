#include "../push_swap.h"

int	ft_lstsize(t_stack *lst)
{
	t_stack	*a;
	int		i;

	i = 0;
	a = lst;
	while (a != NULL)
	{
		a = a->next;
		i++;
	}
	return (i);
}
